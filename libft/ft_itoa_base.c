#include "implemt.h"

char	*ft_itoa_base(long int value, char *base_digits)
{
	int		i;
	char	*nbr;
	int		neg;
	int		base;

	base = ft_strlen(base_digits);
	i = 1;
	neg = 0;
	if (value < 0)
	{
		neg = (base == 10) ? (1) : (0);
		value *= -1;
	}
	while (ft_pow(base, i) - 1 < value)
		i++;
	nbr = (char*)malloc(sizeof(nbr) * i);
	nbr[i + neg] = '\0';
	while (i-- > 0)
	{
		nbr[i + neg] = (value % base) + (value % base > 9 ? 'A' - 10 : '0');
		value = value / base;
	}
	if (neg)
		nbr[0] = '-';
	return (nbr);
}
