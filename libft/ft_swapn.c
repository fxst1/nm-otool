#include "implemt.h"

void	ft_swapn(void *a, void *b, size_t len)
{
	char	tmp;

	while (len)
	{
		tmp = *(char*)b;
		*(char*)(b++) = *(char*)a;
		*(char*)(a++) = tmp;
		len--;
	}
}
