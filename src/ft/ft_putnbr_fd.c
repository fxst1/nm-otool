#include <libft.h>

void	ft_putnbr_fd(long n, int fd)
{
	if (n <= 2147483647 && n > -2147483648)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
		else
		{
			n += '0';
			write(fd, &n, fd);
		}
	}
	else
		write(fd, "-2147483648", 11);
}
