#include <libft.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while ((*s1 == *s2 && *s1) && (--n))
	{
		s1++;
		s2++;
	}
	return ((n == 0) ? 0 : (unsigned char)*s1 - (unsigned char)*s2);
}
